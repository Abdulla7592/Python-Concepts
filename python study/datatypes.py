print(x)
x=4#integer
print(x)
x=40.6#float
print(x)
x=-2+3j#complex
print(x)
x=["apple","ornage","banana"]#list a sequence type
print(x)
x=[1,2,3,4,5]#list a sequence type
print(x)
x=("apple","orange","banana")#tuple a sequence type
print(x)
x=range(10)# range a sequence type
print(x)
x={"Name":"abdulla","age":24}#dict mapping type
print(x)
x={"geeks","for","geeks"}#set
print(x)
x=frozenset({"geeks","for","geeks"})#frozenset 
print(x)
x=True#bool
print(x)
x=False#bool
print(x)
x=b"geeks"#bytes a binary type
print(x)
x=bytearray(5)#bytearray a binary type
print(x)
x=memoryview(bytes(5))#memoryview a binary type
print(x)
